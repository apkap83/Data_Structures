// Αλγόριθμος του Ευκλείδη για τον υπολογισμό του ΜΚΔ
function gcd2(x: number, y: number) {
  debugger;
  let z;
  while (z !== 0) {
    z = x % y; // Υπολογισμός του υπολοίπου
    x = y; // Αντικατάσταση του x με το y
    y = z; // Αντικατάσταση του y με το υπόλοιπο
  }
  return x; // Όταν το y γίνει 0, το x είναι ο ΜΚΔ
}

// Παράδειγμα χρήσης
console.log(gcd2(150, 35)); // 5
